# Operador lógico AND
if True and True:
    print("Esse bloco 1 AND será executado")

if True and False:
    print("Esse bloco 2 AND não será executado")

if False and False:
    print("Esse bloco 3 AND não será executado")

# Operador lógico OR
if True or True:
    print("Esse bloco 1 OR será executado")

if True or False:
    print("Esse bloco 2 OR será executado")

if False or False:
    print("Esse bloco 3 OR não será executado")